class Solution{
public:
    int findMin(int arr[], int n){
        //complete the function here
        int b=0, e=n-1;
        while(b<e){
            int m=(b+e)/2;
            if(arr[m]>arr[e]) b=m+1;
            else e=m;
        }
        return arr[b];
    }
};

